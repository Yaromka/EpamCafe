package com.epam.cafe.entity;

public class Category extends AbstractEntity{
    private int id;
    private String name;

    public Category() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object anotherObject) {
        if (this == anotherObject) {
            return true;
        }
        if (!(anotherObject instanceof Category)) {
            return false;
        }

        Category anotherCategory = (Category) anotherObject;

        return id == anotherCategory.id &&
               (name != null ? name.equals(anotherCategory.name) : anotherCategory.name == null);
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}